print(f"    S_rad = (4/3)S_BH   = {sci(S_rad_zurek)}  k_B   (Zurek 1982)")
print(f"    net dS generated    = (1/3)S_BH = {sci(dS_zurek)}  k_B  (arrow measure)")
print()
print("[A] BRANCH A  plateau -> inflation -> reheating (T_reh=1e15 GeV, g*=106.75):")
print(f"    H_reheat            = {sci(H_reh)} GeV")
print(f"    S_A (one horizon)   = {sci(S_A_horizon)}  k_B")
print(f"    S_A (obs. universe, comoving cross-check) ~ {sci(S_A_comoving_obs)} k_B")
print()
print("[B] BRANCH B  erebon mass term (cold, m~M_P, w=0, NO reheating bath):")
print(f"    S_A/S_B per unit energy = m_ereb/T_reh = {sci(ratio_per_energy)}")
print(f"    S_B (same horizon energy) = {sci(S_B_horizon)}  k_B")
print()
print("[1] RANKING:")
print(f"    S_A / S_B          = {sci(S_A_horizon/S_B_horizon)}")
print(f"    -> plateau branch produces ~{sci(S_A_horizon/S_B_horizon)}x MORE entropy.")
print(f"    MEPP would RANK plateau > erebon. Unambiguous ranking.")
print()
print("[2] SELECTION test -- is the max BOUNDED / does it fix a finite amplitude?")
print("    S_A vs e-folds N (monotone increasing, no interior maximum):")
for N, S in efold_scan:
    print(f"      N={N:3d}   S_A ~ {sci(S)}  k_B")
