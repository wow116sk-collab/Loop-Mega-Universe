#!/usr/bin/env python3
"""
CLOSURE ATTEMPT B -- entropy-production / MEPP selection for the LMU wiring joint.

Question tested: does requiring MAXIMAL entropy production across the flash
UNIQUELY select the plateau-inflation branch (over the erebon mass-term branch),
and/or fix Omega's Cauchy data (phi_1, phi_2)?

We build TWO entropy-production functionals with LMU's own numbers and compare.
Skeptical stance: MEPP has no accepted derivation (Martyushev-Seleznev 2006;
Dewar's MaxEnt attempt judged incomplete; Lineweaver: MEPP fails for cosmological
IC = no fixed boundary conditions). So a numerical winner is a RANKING, and we
explicitly test whether it is also a SELECTION (unique, bounded, fixes phi_1).

All k_B = 1 (entropy in nats/particle-equivalents). Natural units GeV where useful.
No external deps beyond math.
"""
import math

# ---------------------------------------------------------------------------
# Physical constants (SI + natural units)
# ---------------------------------------------------------------------------
G_SI     = 6.674e-11          # m^3 kg^-1 s^-2
hbar_SI  = 1.055e-34          # J s
c_SI     = 2.998e8            # m/s
kB_SI    = 1.381e-23          # J/K
m_P_kg   = 2.176e-8           # Planck mass (kg)
M_sun    = 1.989e30           # kg
M_P_GeV  = 1.221e19           # Planck mass (GeV), reduced would be 2.4e18
GeV_in_K = 1.160e13           # 1 GeV -> K
GeV_in_invm = 5.068e15        # 1 GeV -> 1/m  (hbar c = 0.1973 GeV*fm)

# ---------------------------------------------------------------------------
# LMU / repo figures
# ---------------------------------------------------------------------------
T_reh_GeV   = 1e15            # reheating temperature (LMU figure ~10^15 GeV)
g_star      = 106.75          # relativistic dof at reheating (SM)
M_survivor  = 1e11 * M_sun    # last surviving supermassive BH mass (~10^11 Msun)
m_erebon_GeV= M_P_GeV         # Penrose erebon: ~Planck mass, cold (dust, w=0)

# ---------------------------------------------------------------------------
# (0) Survivor black-hole entropy  S_BH = 4 pi (M/m_P)^2   [k_B units]
#     and Zurek laundering  S_rad = (4/3) S_BH  (net +1/3 S_BH generated)
# ---------------------------------------------------------------------------
S_BH   = 4.0 * math.pi * (M_survivor / m_P_kg)**2
S_rad_zurek = (4.0/3.0) * S_BH
dS_zurek    = (1.0/3.0) * S_BH      # net entropy GENERATED by evaporation

# ---------------------------------------------------------------------------
# BRANCH A -- plateau inflation -> reheating.
# Functional: the flash kicks omega onto the alpha=1 plateau; it inflates,
# then reheats a horizon patch to T_reh, thermalizing vacuum energy into a
# relativistic bath. Entropy-production functional = radiation entropy created.
#
#   entropy density  s = (2 pi^2/45) g_* T^3          (natural units, GeV^3)
#   energy density   rho = (pi^2/30) g_* T^4
#   Hubble at reheat H^2 = rho / (3 Mpl_red^2)  ~  rho/(3 (M_P/sqrt(8pi))^2)
#   horizon volume   V_H = (4 pi/3) H^-3
#   S_A(horizon)     = s * V_H
# We also give the comoving/observable-universe number (adiabatically conserved
# after reheating) ~ today's radiation entropy for cross-check.
# ---------------------------------------------------------------------------
def branch_A_horizon_entropy(T_GeV, g=g_star):
    Mpl_red_GeV = M_P_GeV / math.sqrt(8.0*math.pi)      # reduced Planck mass
    s_dens_GeV3  = (2.0*math.pi**2/45.0) * g * T_GeV**3   # GeV^3
    rho_GeV4     = (math.pi**2/30.0) * g * T_GeV**4       # GeV^4
    H_GeV        = math.sqrt(rho_GeV4 / (3.0*Mpl_red_GeV**2))
    # convert to inverse metres then metres
    H_invm       = H_GeV * GeV_in_invm
    V_H_m3       = (4.0*math.pi/3.0) * H_invm**(-3)
    # entropy density in 1/m^3:  s(GeV^3) * (GeV_in_invm)^3
    s_dens_invm3 = s_dens_GeV3 * GeV_in_invm**3
    S_horizon    = s_dens_invm3 * V_H_m3
    return S_horizon, H_GeV

S_A_horizon, H_reh = branch_A_horizon_entropy(T_reh_GeV)

# Comoving observable-universe radiation entropy (standard result, cross-check):
# today ~ 1.5e88 (CMB) + neutrinos -> ~2.9e88 ; often quoted ~1e88-1e90.
S_A_comoving_obs = 2.9e88

# ---------------------------------------------------------------------------
# BRANCH B -- erebon mass term (Penrose dark matter).
# omega becomes a Planck-mass massive scalar (cold, w=0). NO reheating bath:
# rest-mass gas, entropy ~ number of quanta * O(1), no thermalization.
# For the SAME energy budget E that branch A thermalizes at T_reh, compare:
#   radiation: N_rad ~ E / T_reh   quanta  -> S ~ N_rad   (S ~ E/T, k_B=1)
#   erebons:   N_ereb ~ E / m_ereb quanta  -> S ~ N_ereb  (cold dust, ~1 nat each)
# So per unit energy, S_A/S_B ~ m_ereb / T_reh.
# ---------------------------------------------------------------------------
ratio_per_energy = m_erebon_GeV / T_reh_GeV     # entropy advantage of A over B
# Scale branch A horizon entropy down by that ratio to get the erebon-branch
# entropy for the SAME horizon energy budget:
S_B_horizon = S_A_horizon / ratio_per_energy

# ---------------------------------------------------------------------------
# (2) MONOTONICITY / SELECTION test.
# entropy production of branch A grows with reheating volume ~ e^{3 N_efold}
# (more e-folds -> more reheated comoving volume -> more entropy). Show that
# "maximize S" is UNBOUNDED in N: it does NOT pick a finite plateau amplitude.
# ---------------------------------------------------------------------------
def S_A_vs_efolds(N):
    # comoving entropy after reheating scales with the volume inflated then
    # reheated; take S ~ S0 * exp(3*(N - N0)) with N0=60 anchor at S_A_comoving_obs
    N0 = 60.0
    return S_A_comoving_obs * math.exp(3.0*(N - N0))

efold_scan = [(N, S_A_vs_efolds(N)) for N in (60, 65, 70, 80, 100)]

# ---------------------------------------------------------------------------
# REPORT
